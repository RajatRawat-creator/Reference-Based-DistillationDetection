#!/usr/bin/env python3
"""
Dev tool: (re)generate results_manifest.json from the committed ReferenceMIAResults
tree. Run this ONLY when the curated layout changes; the committed manifest is what
gather_results.py reads (it must not depend on the tree it is rebuilding).

Each curated result JSON is identified by a content key derived from its own
contents -- (model_name, ref_model, sorted candidate-key list) -- which is unique
across the tree (verified). The manifest maps that key's hash to the literal
checked-in relative path, so gather copies a freshly-computed output to the exact
committed location with the exact committed filename (no naming rules to encode).

Files carrying `source_target_json`/`note` are MoE-16B combine artifacts (the two
DeepSeek-R1 ref:MoE-16B results): built by `_build_from_collected_losses.py` from
OLD out-of-tree collected losses, intentionally static (see project note
moe16b_o1_scoring_incompat). They are recorded with source="combine" and skipped
by gather.
"""
import json
import glob
import hashlib
import os
from pathlib import Path

ROOT = Path(__file__).resolve().parent.parent          # ReferenceMIAResults/
OUT = Path(__file__).resolve().parent / "results_manifest.json"


def candidate_keys(d):
    r = d.get("results", {})
    blk = r.get("Ref-Norm Loss", r) if isinstance(r, dict) else {}
    return sorted(blk.keys()) if isinstance(blk, dict) else []


def content_key(model_name, ref_model, ckeys):
    payload = json.dumps([model_name, ref_model, sorted(ckeys)], sort_keys=True)
    return hashlib.sha1(payload.encode()).hexdigest()


def main():
    entries = {}
    for fp in sorted(glob.glob(str(ROOT / "**" / "*.json"), recursive=True)):
        rel = os.path.relpath(fp, ROOT)
        if rel.startswith(("scripts/", "figures/")):
            continue
        d = json.load(open(fp))
        if "model_name" not in d:
            continue
        ck = candidate_keys(d)
        key = content_key(d.get("model_name"), d.get("ref_model"), ck)
        source = "combine" if ("source_target_json" in d or "note" in d) else "run_script"
        if key in entries:
            raise SystemExit(f"DUPLICATE content key for {rel} and {entries[key]['dest']}")
        entries[key] = {
            "dest": rel,
            "model_name": d.get("model_name"),
            "ref_model": d.get("ref_model"),
            "candidate_keys": ck,
            "source": source,
        }
    manifest = {
        "_README": "Maps content-key (sha1 of [model_name, ref_model, sorted candidate keys]) "
                   "-> curated relative path under ReferenceMIAResults/. Generated by "
                   "_gen_results_manifest.py; consumed by gather_results.py.",
        "entries": entries,
    }
    json.dump(manifest, open(OUT, "w"), indent=2, ensure_ascii=False)
    n_run = sum(1 for e in entries.values() if e["source"] == "run_script")
    n_comb = sum(1 for e in entries.values() if e["source"] == "combine")
    print(f"wrote {OUT.name}: {len(entries)} entries ({n_run} run_script, {n_comb} combine)")


if __name__ == "__main__":
    main()
